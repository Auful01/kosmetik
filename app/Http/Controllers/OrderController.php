     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // return $request;
        $user = auth()->user()->id;
        Order::create([
            'user_id' => $user,
            'doctor_id' => $request->doctor_id,
            'date' => $request->date,
            'time' => $request->time,
            'total' => $request->price,
        ]);
        return redirect()->route('transaction.index');
    }

